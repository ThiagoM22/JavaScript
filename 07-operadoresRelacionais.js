console.log("x é diferente de y?", x !== y); //!= é diferente

console.log("x é maior que y", x > y);
console.log("x é menor que y", x < y);
console.log("x é maior ou igual a y", x >= y);
console.log("x é menor ou igual a y", x <= y);

